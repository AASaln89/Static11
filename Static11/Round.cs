using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Static11
{
    static public class Round
    {
        public static double Okr(double r)
        {
            return 2 * r * Math.PI;
        }
        public static double S(double r)
        {
            return Math.PI * r * r;
        }
    }
}
